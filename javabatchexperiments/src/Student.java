
public class Student {
	
	int id;
	String name;
	int score;
	
	int getId() {
		return id;
	}
	
	void setId(int idArg) {
		id=idArg;
	}
	
	String getName() {
		return name;
	}
	
	void setName(String nameArg) {
		name=nameArg;
	}
	
	int getScore() {
		return score;
	}
	
	void setScore(int scoreArg) {
		score=scoreArg;
	}
	
}
